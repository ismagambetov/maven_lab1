package main.java.kz.epam.lab.data;

/**
 * @author Damir Ismagambetov
 * 
 * Class with values for food mode
 * including some method that return values
 */
public final class FoodMode {	
	private int RO = 0;
	private int BB = 10;
	private int HB = 20;
	private int FB = 30;
	private int AL = 40;
	
	/**
	 * @return food mode - RO
	 */	
	public int getRO() {
		return RO;
	}
	
	/**
	 * @return food mode - BB
	 */	
	public int getBB() {
		return BB;
	}
	
	/**
	 * @return food mode - HB
	 */	
	public int getHB() {
		return HB;
	}
	
	/**
	 * @return food mode - FB
	 */	
	public int getFB() {
		return FB;
	}
	
	/**
	 * @return food mode - AL
	 */	
	public int getAL() {
		return AL;
	}
	
	
}
	